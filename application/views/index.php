			The Chambly Ball Hockey League started in Chambly, QC in 1980 as weekly friendly pick-up games. Today the league consists of 5 teams, where 4 teams, on rotation, play each other (2 games) every Saturday morning at 10:00 with the 5th team's players acting as referees. Seasons start in October and end in May. Playoffs then follow a week after the last regular season.
		</br>
			The game is played 4 on 4 outside on an asphalt surface. Rain or shine ...or snowstorm, matches are held regardless of weather conditions.
